package com.uway.sports.web.entity.paraentity;

public class NoticeTaskResult {

    private Integer mainId;
    private Integer mainUserId;
    private Integer cnt;
    private String cntIdStr;

    public Integer getMainId() {
        return mainId;
    }

    public void setMainId(Integer mainId) {
        this.mainId = mainId;
    }

    public Integer getMainUserId() {
        return mainUserId;
    }

    public void setMainUserId(Integer mainUserId) {
        this.mainUserId = mainUserId;
    }

    public Integer getCnt() {
        return cnt;
    }

    public void setCnt(Integer cnt) {
        this.cnt = cnt;
    }

    public String getCntIdStr() {
        return cntIdStr;
    }

    public void setCntIdStr(String cntIdStr) {
        this.cntIdStr = cntIdStr;
    }
}
