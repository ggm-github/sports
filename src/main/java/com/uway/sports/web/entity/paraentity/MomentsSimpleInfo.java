package com.uway.sports.web.entity.paraentity;

public class MomentsSimpleInfo {

    private Integer id;
    private Integer hotSet;
    private Integer replyCnt;
    private Integer goodCnt;
    private Integer voteCnt;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getHotSet() {
        return hotSet;
    }

    public void setHotSet(Integer hotSet) {
        this.hotSet = hotSet;
    }

    public Integer getReplyCnt() {
        return replyCnt;
    }

    public void setReplyCnt(Integer replyCnt) {
        this.replyCnt = replyCnt;
    }

    public Integer getGoodCnt() {
        return goodCnt;
    }

    public void setGoodCnt(Integer goodCnt) {
        this.goodCnt = goodCnt;
    }

    public Integer getVoteCnt() {
        return voteCnt;
    }

    public void setVoteCnt(Integer voteCnt) {
        this.voteCnt = voteCnt;
    }
}
